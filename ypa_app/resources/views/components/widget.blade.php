        await fetch('http://127.0.0.1:8000/api/controls/')
            .then(response => {
                if (!response.ok) {
                    throw new Error('Network response was not ok');
                }
                return response.json();
            })
            .then(data => {
                var isChecked = '';
                data.data.forEach(element => {

                    if (element.category === 'banner_controller' && element.status === 'TRUE') {
                        $('#banner_file_path').attr('src', element.file_path)
                        $('#banner_file_path').parent().attr('href', element.link === 'NA' ? '' :
                            element.link)

                        setTimeout(() => {

                            $('.cover-widget').removeClass('hidden').addClass('flex');
                            $('.cover-widget').animate({
                                marginTop: "0px",
                            })
                        }, 1000);
                    } else {}





                })
            })
            .catch(error => {
                console.error('Error:', error);
                // Handle error (e.g., show an error message)
            });




    }
    fetchBanner();
