            scrollToTop();
        })

        // Show the button when the user scrolls down 20px from the top
        $(window).scroll(function() {
            if ($(this).scrollTop() > 30) {
                mybutton.show();
            } else {
                mybutton.hide();
            }
        });

        // Scroll to the top of the document when the button is clicked
        function scrollToTop() {
            $("html, body").animate({
                scrollTop: 0
